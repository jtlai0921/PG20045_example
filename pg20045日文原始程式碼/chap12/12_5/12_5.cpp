int main(void)
{
	Car		benz(90.0, 0.0, 0.0);
	char	cont;	// 処理を続行するか

	while (1) {
		cout << benz;

		cout << "aaa";
		cin >> cont;

		if (cont == 'N' || cont == 'n') break;

		double  dx, dy;
		cout << "bbb";	cin >> dx;
		cout << "ccc";	cin >> dy;

		if (!benz.Move(dx, dy))
			cout << "dddddd\n";
	}

	return (0);
}

